import bz2
import binascii


def split80(s):
    return '\n'.join([s[i:i+80] for i in range(0, len(s), 80)])


def encode(s):
    return split80(binascii.b2a_base64(bz2.compress(s.encode('latin-1'))))


def decode(s):
    return bz2.decompress(binascii.a2b_base64(s)).decode().lstrip()


